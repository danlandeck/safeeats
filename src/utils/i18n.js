// Locale → BCP 47 tag for Intl.DateTimeFormat
export const LANG_TO_LOCALE = {
  en: "en-US", fr: "fr-FR", de: "de-DE", es: "es-ES", it: "it-IT",
  pt: "pt-BR", nl: "nl-NL", ar: "ar-AE", zh: "zh-CN", ja: "ja-JP",
  ko: "ko-KR", hi: "hi-IN", tr: "tr-TR", el: "el-GR", th: "th-TH",
};

// Format a date string using the user's locale
export function formatLocalDate(dateStr, langCode, options = { year: "numeric", month: "short", day: "numeric" }) {
  if (!dateStr) return "";
  try {
    const locale = LANG_TO_LOCALE[langCode] || "en-US";
    return new Intl.DateTimeFormat(locale, options).format(new Date(dateStr));
  } catch {
    return dateStr;
  }
}

export function getTranslations(regionKey) {
  const locale = REGION_LOCALE[regionKey] || "en";
  return TRANSLATIONS[locale] || TRANSLATIONS["en"];
}

